            <h1>mlr (Machine Learning in R)</h1>
          </div>
	The R package <a href="https://github.com/berndbischl/mlr">mlr</a> interfaces a large number of classification and regression techniques. It also uses the <a href="#r" role="tab" data-toggle="tab" target="_blank">OpenML R</a> package (by the same authors) to interface seamlessly with OpenML. This means you can download data and tasks from OpenML, run the many mlr algorithms, and organize all ensuing results online with a few lines or R.

	<h2 id="r-download">Download</h2>
	You'll need the <a href="https://github.com/berndbischl/mlr">mlr</a> and <a href="#r" role="tab" data-toggle="tab" target="_blank">openml</a> packages. Soon, both will be available from CRAN.

	<h2 id="r-start">Quick Start</h2>
	In <a href="https://github.com/openml/r/blob/master/doc/knitted/1-Introduction.md" target="_blank">this tutorial</a>, you can find examples of standard use cases.

	<h2 id="r-issues">Issues</h2>
	Having questions? Did you run into an issue? Let us know via the <a href="https://github.com/openml/r/issues"> OpenML R issue tracker</a>.
